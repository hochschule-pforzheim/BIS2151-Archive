    private void okayButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okayButtonActionPerformed

        String description = descriptionTextField.getText();
        System.out.println(description);

        if (description.isEmpty()) {
            description = JOptionPane.showInputDialog("Description is empty");
        }
        
        int answer = JOptionPane.showConfirmDialog(this, "Test");
        if (answer == JOptionPane.YES_OPTION) {
            System.out.println("Yes!");
        }

        Room newRoom = new Room(description, null);
        theRoomManager.addRoom(newRoom);
        
        descriptionTextField.setText("");

        idLabel.setText(String.valueOf(theRoomManager.getRooms().size()));
    }//GEN-LAST:event_okayButtonActionPerformed

