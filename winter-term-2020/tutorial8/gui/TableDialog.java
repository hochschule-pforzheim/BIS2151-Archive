    public void addRowToJTable()
    {
        DefaultTableModel model = (DefaultTableModel) tableBike.getModel();
        ArrayList<Bike> localbikes = tutorial8.logic.BikeManager.getBIKES();

        // demo content
        Bike bike01 = new Bike("Super Bike", 550.0);
        Bike bike02 = new Bike("Normal Bike", 360.0);
        Bike bike03 = new Bike("Deluxe Bike", 2900.0);
        localbikes.add(bike01);
        localbikes.add(bike02);
        localbikes.add(bike03);

        Object rowData[] = new Object[3];

        for (int i = 0; i < localbikes.size(); i++)
        {
            rowData[0] = localbikes.get(i).getBikeID();
            rowData[1] = localbikes.get(i).getBikeDescription();
            rowData[2] = localbikes.get(i).getBikePrice();
            model.addRow(rowData);

        }
    }

